LAT_DIM = 2
CAPACITY = 64
LRN_RATE = 1e-3
VAR_BETA = 1

class Encoder(nn.Module):
    def __init__(self):
        super(Encoder, self).__init__()
        
        self.hidden1 = nn.Linear(
            in_features = 128,
            out_features = CAPACITY,
        )
        self.dropout1 = nn.Dropout(0.5)
        self.bn1 = nn.BatchNorm1d(CAPACITY)
        self.fc_mu = nn.Linear(
            in_features = CAPACITY,
            out_features = LAT_DIM,
        )
        self.fc_logvar = nn.Linear(
            in_features = CAPACITY,
            out_features = LAT_DIM,
        )
        
    def forward(self, x):
        x = self.hidden1(x)
        #x = self.bn1(x)
        x = F.relu(x)
        #x = self.dropout1(x)
        x_mu = self.fc_mu(x)
        x_logvar = self.fc_logvar(x)
        return x_mu, x_logvar

class Decoder(nn.Module):
    def __init__(self):
        super(Decoder, self).__init__()
        
        self.hidden1 = nn.Linear(
            in_features = LAT_DIM,
            out_features = CAPACITY,
        )
        self.dropout1 = nn.Dropout(0.5)
        self.bn1 = nn.BatchNorm1d(CAPACITY)
        self.output = nn.Linear(
            in_features = CAPACITY,
            out_features = 128,
        )

    def forward(self, x):
        x = self.hidden1(x)
        #x = self.bn1(x)
        x = F.relu(x)
        #x = self.dropout1(x)
        x = self.output(x)
        x = x.view(x.size(0), 128)
        return x

class VariationalAutoencoder(nn.Module):
    def __init__(self):
        super(VariationalAutoencoder, self).__init__()
        
        self.encoder = Encoder()
        self.decoder = Decoder()

    def forward(self, x):
        latent_mu, latent_logvar = self.encoder(x)
        latent = self.latent_sample(latent_mu, latent_logvar)
        x_recon = self.decoder(latent)
        return x_recon, latent_mu, latent_logvar

    def latent_sample(self, mu, logvar):
        if self.training:
            # the reparameterization trick
            std = logvar.mul(0.5).exp_()
            eps = torch.empty_like(std).normal_()
            return eps.mul(std).add_(mu)
        else:
            return mu

def reconstruction_error(recon_x, x):
    return F.mse_loss(
        recon_x.view(-1, 128),
        x.view(-1, 128),
        reduction = "sum",
    )

def vae_loss(recon_loss, mu, logvar):
    kl_divergence = -0.5 * torch.sum(1 + logvar - mu.pow(2) - logvar.exp())
    return recon_loss + VAR_BETA * kl_divergence

